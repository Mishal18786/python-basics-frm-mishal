'''more on strings'''

import math

pi = math.pi

print(f'pi is eqal to {pi}')

print(f'pi is eqal to {pi:.03f}')

print(f'pi is eqal to {pi:12.3f}')

print(f'pi is eqal to {pi:012.3}')

print(f'pi is eqal to {pi:+.3f}')

print(f'pi is eqal to {pi:>10.3f}')

print(f'pi is eqal to {pi:^10.3f}')

print(f'pi is eqal to {pi:<10.3f}')

print(f'pi is eqal to {pi:=+10.3f}')
